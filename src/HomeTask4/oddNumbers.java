package HomeTask4;

public class oddNumbers {
    public static void main(String[] args) {
        for(int i = 20; i <= 50; i++) {
            if(i%2 != 0) {
                System.out.println(i);
            }
        }
        for(int i = 20; i <= 50; i++) {
            if(i%2 != 0) {
                System.out.print(i + " ");
            }
        }
    }
}
